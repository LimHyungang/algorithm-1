from sys import stdin

N = int(stdin.readline())
screen = [list(stdin.readline().rstrip()) for _ in range(N)]


def divide_screen(x, y, n):
    global ans
    check = set()
    for i in range(x, x+n):
        for j in range(y, y+n):
            check.add(screen[i][j])
    # 해당 부분 값이 모두 같은지 확인, 다르면 재귀 호출
    if len(check) == 1:
        ans += screen[x][y]
    else:
        nn = n // 2
        ans += "("
        divide_screen(x, y, nn)
        divide_screen(x, y+nn, nn)
        divide_screen(x+nn, y, nn)
        divide_screen(x+nn, y+nn, nn)
        ans += ")"


ans = ""
divide_screen(0, 0, N)
print(ans)

